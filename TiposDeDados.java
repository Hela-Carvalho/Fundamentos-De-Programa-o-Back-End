public class TiposDeDados {
    public static void main(String[] args) {
        byte idade = 19;
        System.out.println("idade (tipo byte): " + idade);

        short populacaoCidade = 2400;
        System.out.println("População da cidade (tipo short): " + populacaoCidade);

        int populacaoMundial = 700000000;
        System.out.println("populaçã mundial (tipo int): " + populacaoMundial);

        long distanciaEstrelas = 123;
        System.out.println("distancia entre entrelas (tipo long): " + distanciaEstrelas);

        float peso = 105.5f;
        System.out.println("peso (tipo float): " + peso);

        double pi = 3.14;
        System.out.println("numero pi (tipo double): " + pi);

        char inicialNome = 'H';
        System.out.println("inicial do nome (tipo char): " + inicialNome);

        boolean verdade = true;
        boolean falso = false;
        System.out.println("verdade (tipo boolean): " + verdade);
        System.out.println("falso (tipo boolean): " + falso);
        
        String nomeCompleto = "Helã Matos De Carvalho";
        System.out.println("nome completo (tipo string): " + nomeCompleto);
    }

    
}